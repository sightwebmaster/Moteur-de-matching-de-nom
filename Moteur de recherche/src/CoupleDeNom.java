
public class CoupleDeNom {
	private Nom nom1;
	private Nom nom2;
	public CoupleDeNom(Nom nom1, Nom nom2) {
		super();
		this.nom1 = nom1;
		this.nom2 = nom2;
	}
	public Nom getNom1() {
		return nom1;
	}
	public void setNom1(Nom nom1) {
		this.nom1 = nom1;
	}
	public Nom getNom2() {
		return nom2;
	}
	public void setNom2(Nom nom2) {
		this.nom2 = nom2;
	}
	
	
}
